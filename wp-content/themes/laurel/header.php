	<h1 style="display: none;">Allure Sauvage  - Designer bags from Switzerland</h1>
	
	<p style="display: none;">Allure Sauvage offers a new style of maroquinerie, without cruelty nor plastic, only plant-based materials. Made in Switzerland by women in professional reintegration.</p>
	
	<p style="display: none;">We are Natacha, graduate of the Ecole Hôtelière de Lausanne and Selena, Master of Luxury Craftsmanship from the Ecole Cantonale D’Arts de Lausanne. </p>
	
	<p style="display: none;">We first met over coffee at Bad Hunter, a vegetarian place in Lausanne, Switzerland.
	Die-hard recycling specialists, environmental activists and meat avoiders, we were both angry not to have other choices  	than leather or plastic when it came to handbags. We’d have some vegan options, Or we’d have some recycled plastic
	options, made in China. There was so little we could find that really conveyed our values, and these few pieces we’d
	have to import from across the world.</p>
	
	<p style="display: none;">This is how Allure Sauvage was created.  Allure represents the pride and elegance
	we both look for when choosing our clothes and accessories, and Sauvage the relationship we have with nature itself. </p>
	<p style="display: none;">We aim to only use plant-based materials and turn them into hip things, not hippie.
	As well as being vegan and without plastic, our bags are fair:we collaborate with a geneva-based workshop
	helping women back into the professional life, awarded for their ethics and sustainable development. </p>
	
	<p style="display: none;">At Allure Sauvage, we provide ethically-made, designer products with plant-based 
	alternatives to leather and plastic and a reduced carbon footprint.</p>
	
	<p style="display: none;">We were lucky to be helped by amazing sponsors to achieve this campaign and would like to use this opportunity to thank them once again. Don’t forget to post your purchases with the hashtag #jesuissauvage </p>
	
	<p style="display: none;">Allure Sauvage. Designed and Developed by Webbinart</p>
	
	<p style="display: none;">We provide the sophisticated woman high-end ethical designer products with plant-based alternatives to leather and plastic for a sustainable wardrobe. Our first collection, the Peacock Lady, is handmade in Switzerland with Piñatex. PETA vegan approved.</p>
	
	<p style="display: none;">Our first collection is made with pineapple leaf fibers, Piñatex. We will later experiment with other plant-based textiles. For more information about Piñatex, check the blog post written by Daniela!</p>
	
